    <script>
      const sidebar = document.getElementById("sidebar");
      const content = document.getElementById("content");

      function showSection(sectionId) {
        const sections = content.children;
        for (let i = 0; i < sections.length; i++) {
          sections[i].style.display = "none";
        }
        document.getElementById(sectionId).style.display = "block";
      }

      sidebar.addEventListener("click", (event) => {
        if (event.target.tagName === "A") {
          showSection(event.target.getAttribute("href").substr(1));
        }
      });

      showSection("account"); // Tampilkan Akun Saya secara default
    </script>
    <script>
      // Menampilkan dialog pemilihan file saat gambar di klik
      document
        .getElementById("previewProfileImage")
        .addEventListener("click", function () {
          document.getElementById("profilePhoto").click();
        });

      // Menampilkan gambar yang dipilih oleh pengguna
      document
        .getElementById("profilePhoto")
        .addEventListener("change", function () {
          const fileInput = this;
          const previewProfileImage = document.getElementById(
            "previewProfileImage"
          );

          if (fileInput.files && fileInput.files[0]) {
            const reader = new FileReader();

            reader.onload = function (e) {
              previewProfileImage.src = e.target.result;
            };

            reader.readAsDataURL(fileInput.files[0]);
          }
        });
    </script>
